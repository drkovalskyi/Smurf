Double_t InterfgHHSystematics(Int_t mH) {
    if(mH<400) return 1.0;
    Int_t mHiggs[29] = {400,420,440,450,460,480,500,520,540,550,560,580,600,620,640,660,680,700,720,740,760,780,800,820,840,860,880,900,1000};
    Double_t kappa[29] = {0.9979665200,0.9973644000,1.0163872112,1.0264436689,1.0365001265,1.0652680626,1.0932697009,1.1217136719,1.1457909461,1.1622655466,1.1787401470,1.2145965883,1.2697380560,1.3130814036,1.3592299637,1.4082594910,1.4603888461,1.5160605911,1.5751212640,1.6376827699,1.7039012956,1.7740012982,1.8482963461,1.9258143934,2.0067943194,2.0921212543,2.1819660879,2.2618724091,2.3417787303};

    Int_t massIndex = -1;
    for (UInt_t m=0; m < 29 ; ++m) {
        if (mH == mHiggs[m]) massIndex = m;
    }
    assert(massIndex >= 0);
    return kappa[massIndex];
}
