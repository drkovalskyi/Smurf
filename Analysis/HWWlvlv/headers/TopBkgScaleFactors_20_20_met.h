static Double_t TopBkgScaleFactor(Int_t jetBin) {
  assert(jetBin >=0 && jetBin <= 2);
  Double_t TopBkgScaleFactor[3] = { 1.12001, 1.07975, 1.1294   };
  return TopBkgScaleFactor[jetBin];
}

